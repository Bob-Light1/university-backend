'use strict';

/**
 * @file document.subschemas.js
 * @description Shared Mongoose sub-schemas for the Document module.
 *
 * WHY THIS FILE EXISTS
 * --------------------
 * ContentBlockSchema, BrandingSchema, and PrintConfigSchema were previously
 * duplicated verbatim in three separate files:
 *   - document.model.js
 *   - documentTemplate.model.js
 *   - documentVersion.model.js
 *
 * Duplication = divergence risk: if an enum value is added (e.g., a new block
 * type), the developer must update three files. One miss = silent inconsistency
 * between Document.body and DocumentVersion.body.
 *
 * This file is the single source of truth. All three models import from here.
 *
 * NOTE: Mongoose sub-schemas can be safely reused across models without
 * registering them as top-level models. The { _id: false } option ensures no
 * extra _id fields are injected on sub-documents.
 */

const mongoose = require('mongoose');

// ── ContentBlock ───────────────────────────────────────────────────────────────

/**
 * Supported block types for the rich-content document body.
 * Exported so document.validation.service.js can reference this list directly.
 *
 * Supported block types and their expected content shapes:
 *   HEADING              → { level: 1|2|3, text, align }
 *   PARAGRAPH            → { text, align, color, fontSize, bold, italic }
 *   IMAGE                → { fileName, alt, width, caption }
 *   TABLE                → { headers: [], rows: [[]], striped }
 *   LIST                 → { items: [], ordered }
 *   QR_CODE              → { data, size, label }
 *   CODE_BLOCK           → { code, language }
 *   DIVIDER              → {}
 *   SIGNATURE_PLACEHOLDER → { label }
 */
const CONTENT_BLOCK_TYPES = Object.freeze([
  'HEADING',
  'PARAGRAPH',
  'IMAGE',
  'TABLE',
  'LIST',
  'CODE_BLOCK',
  'DIVIDER',
  'QR_CODE',
  'SIGNATURE_PLACEHOLDER',
]);

/**
 * ContentBlock — one unit of the rich-content document body.
 * `content` is stored as Mixed; per-type validation runs at service layer
 * via document.validation.service.js (discriminated schemas).
 */
const ContentBlockSchema = new mongoose.Schema({
  /** UUID generated by client or server at block creation */
  blockId: { type: String, required: true },

  type: {
    type:     String,
    required: true,
    enum:     CONTENT_BLOCK_TYPES,
  },

  /** Zero-based position within the document body */
  order: { type: Number, required: true },

  /** Per-type content — validated at service layer before any DB write */
  content: { type: mongoose.Schema.Types.Mixed },

  /** Optional inline style overrides (e.g., custom padding, background) */
  style: { type: mongoose.Schema.Types.Mixed },

}, { _id: false });

// ── Branding ───────────────────────────────────────────────────────────────────

/**
 * BrandingSchema — visual identity configuration applied at PDF generation time.
 */
const BrandingSchema = new mongoose.Schema({
  logoFileName:   { type: String,  default: null },
  primaryColor:   { type: String,  match: /^#[0-9A-Fa-f]{6}$/, default: '#003366' },
  accentColor:    { type: String,  match: /^#[0-9A-Fa-f]{6}$/, default: '#E8A020' },
  showCampusName: { type: Boolean, default: true },
  showDate:       { type: Boolean, default: true },
  footerText:     { type: String,  maxlength: 200, default: '' },
  headerText:     { type: String,  maxlength: 200, default: '' },
  /** Semi-transparent diagonal text overlay. Common values: 'DRAFT', 'CONFIDENTIAL', 'COPY', 'SPECIMEN' */
  watermark:      { type: String,  default: null },
}, { _id: false });

// ── PrintConfig ────────────────────────────────────────────────────────────────

/**
 * PrintConfigSchema — page layout settings for PDF generation.
 * CARD_CR80 = 85.6 × 54 mm (standard ISO/IEC 7810 ID card format).
 */
const PrintConfigSchema = new mongoose.Schema({
  pageSize:    { type: String, enum: ['A4', 'LETTER', 'A5', 'CARD_CR80', 'CUSTOM'], default: 'A4' },
  orientation: { type: String, enum: ['PORTRAIT', 'LANDSCAPE'],                     default: 'PORTRAIT' },
  margins: {
    top:    { type: Number, default: 20 },
    right:  { type: Number, default: 20 },
    bottom: { type: Number, default: 20 },
    left:   { type: Number, default: 20 },
  },
  copies: { type: Number, default: 1, min: 1, max: 100 },
}, { _id: false });

// ── Exports ───────────────────────────────────────────────────────────────────

module.exports = {
  ContentBlockSchema,
  BrandingSchema,
  PrintConfigSchema,
  CONTENT_BLOCK_TYPES,
};