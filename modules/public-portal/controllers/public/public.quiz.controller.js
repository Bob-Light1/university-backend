'use strict';

/**
 * @file public.quiz.controller.js
 * @description Public quiz — fetching questions and submitting answers.
 *
 * Routes:
 *  GET  /api/public/quiz?campusSlug=...&category=...&limit=10&lang=fr
 *  POST /api/public/quiz/submit
 *
 * Security / anti-cheat:
 *  - correctIndex is select:false — never included in the questions sent.
 *  - The score is computed entirely on the ERP side; the portal never sees the
 *    correctIndex.
 *  - The session is ISSUED BY THE SERVER at serve time: GET /quiz creates a
 *    `pending` QuizSession storing the exact `servedQuestionIds` and returns a
 *    server-generated sessionToken. Submission scores against that stored set,
 *    so a client cannot shrink the denominator (sending one known-correct answer
 *    no longer yields 100/100) nor swap in questions it never received.
 *
 * sessionToken: UUID generated by the ERP at serve time, echoed back on submit.
 * period       : 'YYYY-MM' computed at serve time and frozen on the session.
 */

const crypto      = require('crypto');
const mongoose    = require('mongoose'); // kept for ObjectId cast/validation
const repo        = require('../../public-portal.repository');
// Lazy require to the campus facade (hub) — see MODULAR_MONOLITH_MIGRATION.md
const campusSvc = () => require('../../../campus').service;

const { asyncHandler, sendSuccess, sendError, sendNotFound, sendConflict } = require('../../../../shared/utils/response-helpers');

// Abandoned pending sessions are reaped by the TTL index after this delay.
const PENDING_SESSION_TTL_MS = 2 * 60 * 60 * 1000; // 2h

/** 'YYYY-MM' for a given date — the monthly leaderboard bucket. */
const periodOf = (date) =>
  `${date.getFullYear()}-${String(date.getMonth() + 1).padStart(2, '0')}`;

// ── GET QUESTIONS ─────────────────────────────────────────────────────────────

const getQuizQuestions = asyncHandler(async (req, res) => {
  const { campusSlug, category, lang = 'fr', limit = '10' } = req.query;

  if (!campusSlug?.trim()) return sendError(res, 400, 'campusSlug is required.');

  const campus = await campusSvc().getActiveCampusBySlug(campusSlug.toLowerCase().trim(), '_id');

  if (!campus) return sendNotFound(res, 'Campus');

  const normalizedCategory = category?.toLowerCase().trim() || null;

  const filter = {
    schoolCampus: campus._id,
    isPublished:  true,
  };
  if (normalizedCategory) filter.category = normalizedCategory;
  if (['fr', 'en'].includes(lang)) filter.lang = lang;

  const limitNum = Math.min(Math.max(parseInt(limit, 10) || 10, 1), 20);

  // $sample aggregation to randomize. The aggregation does NOT respect select:false,
  // so we go through an explicit whitelist: correctIndex and every internal
  // field (schoolCampus, isPublished, timestamps) stay on the ERP side.
  const questions = await repo.sampleQuizQuestions(filter, limitNum);

  if (questions.length === 0) {
    return sendSuccess(res, 200, 'Quiz questions retrieved.', {
      campusSlug:   campusSlug.toLowerCase().trim(),
      category:     normalizedCategory,
      lang,
      sessionToken: null,
      questions:    [],
    });
  }

  // Issue the session: persist the exact served set so the submission can be
  // scored authoritatively. The token is generated here (never trusted from
  // the client) and is the only handle the portal returns at submit time.
  const now          = new Date();
  const sessionToken = crypto.randomUUID();

  await repo.createQuizSession({
    schoolCampus:      campus._id,
    sessionToken,
    status:            'pending',
    category:          normalizedCategory || 'general',
    servedQuestionIds: questions.map((q) => q._id),
    ipAddressHash:     req.ipHash,
    period:            periodOf(now),
    expiresAt:         new Date(now.getTime() + PENDING_SESSION_TTL_MS),
  });

  return sendSuccess(res, 200, 'Quiz questions retrieved.', {
    campusSlug: campusSlug.toLowerCase().trim(),
    category:   normalizedCategory,
    lang,
    sessionToken,
    questions,
  });
});

// ── SUBMIT QUIZ ───────────────────────────────────────────────────────────────

const submitQuiz = asyncHandler(async (req, res) => {
  const {
    sessionToken,
    answers,          // [{ questionId, selectedIndex }]
    displayName,
    city,
    country,
    partnerCode,
  } = req.body;

  if (!sessionToken?.trim())  return sendError(res, 400, 'sessionToken is required.');
  if (!Array.isArray(answers) || answers.length === 0) {
    return sendError(res, 400, 'answers must be a non-empty array.');
  }

  // The session is the source of truth — campus, category and the served
  // question set all come from it, never from the (untrusted) request body.
  const session = await repo.findQuizSessionForWriteByToken(sessionToken.trim());

  if (!session) {
    return sendError(res, 400, 'Invalid or expired quiz session. Please restart the quiz.');
  }
  if (session.status === 'completed' || session.completedAt) {
    return sendConflict(res, 'This quiz session has already been submitted.');
  }

  const servedIds = (session.servedQuestionIds || []).map((id) => String(id));
  if (servedIds.length === 0) {
    return sendError(res, 400, 'This quiz session has no questions to score.');
  }

  // Deduplicate the client answers by questionId (first occurrence wins).
  const answerMap = new Map(); // questionId → selectedIndex
  for (const a of answers) {
    if (a && mongoose.Types.ObjectId.isValid(a.questionId) && !answerMap.has(String(a.questionId))) {
      answerMap.set(String(a.questionId), a.selectedIndex);
    }
  }

  // Score against the SERVED set (the authoritative denominator). Questions are
  // fetched by id within the session's campus — published status at serve time
  // is what counts, so a question unpublished mid-session still scores fairly.
  const questions = await repo.findPublishedQuestionsWithAnswers({
    _id:          { $in: servedIds.map((id) => new mongoose.Types.ObjectId(id)) },
    schoolCampus: session.schoolCampus,
  });

  let correctAnswers = 0;
  for (const q of questions) {
    const selectedIndex = answerMap.get(String(q._id));
    if (typeof selectedIndex === 'number' && selectedIndex === q.correctIndex) {
      correctAnswers++;
    }
  }

  const totalQuestions = servedIds.length;
  const score          = Math.round((correctAnswers / totalQuestions) * 100);
  const now            = new Date();

  // Flip the session to completed and clear the TTL anchor so it is retained.
  session.partnerCode    = partnerCode?.toUpperCase().trim() || session.partnerCode || null;
  session.displayName    = displayName?.trim() || null;
  session.city           = city?.trim() || null;
  session.country        = country?.trim() || null;
  session.score          = score;
  session.correctAnswers = correctAnswers;
  session.totalQuestions = totalQuestions;
  session.completedAt    = now;
  session.status         = 'completed';
  session.expiresAt      = null;

  await repo.saveQuizSessionDoc(session);

  // For placement tests, map score to a campus program recommendation.
  let recommendedProgram = null;
  if (session.category === 'placement') {
    const campus = await campusSvc().getActiveCampusById(session.schoolCampus, '_id programs');
    if (campus?.programs?.length) {
      const programs = campus.programs;
      const idx = Math.min(
        Math.floor((score / 100) * programs.length),
        programs.length - 1,
      );
      recommendedProgram = programs[idx];
    }
  }

  return sendSuccess(res, 201, 'Quiz submitted successfully.', {
    sessionId:         session._id,
    score,
    correctAnswers,
    totalQuestions,
    period:            session.period,
    recommendedProgram,
  });
});

module.exports = { getQuizQuestions, submitQuiz };
